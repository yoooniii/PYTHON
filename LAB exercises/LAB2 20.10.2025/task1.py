#ЗАД 1 // да се напише програма, която принтира n цели числа и намира най-голямото между тях, 
#като първо се въвежда броя на числата n, а след това се въвеждат n цели числа (на нов ред)
 
n=int(input("Въведете n: "))
numbers = []
print("Въведете числата:")
for i in range(n):
    num = int(input())
    numbers.append(num)
print("Въведените числа са:", numbers)
m = max(numbers)
print("Най-голямото число е:", m)
 
