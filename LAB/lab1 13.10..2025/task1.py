# Чете от конзолата а, b, h и пресмята лице на трапец с основи a и b, и височина h. Резултатът да се закръгли до втория знак.
# S=(a+b)h/2

a = float(input("Въведете основа a: "))
b = float(input("Въведете основа b: "))
h = float(input("Въведете височина h: "))
s = (a + b) * h / 2
print(f"Лицето на трапеца е: {round(s, 2)}")
